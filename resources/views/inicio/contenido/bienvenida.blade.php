            R.R. Nº 634/97 del 13 de agosto de 1997 y de la Resolucion del consejo Universitario Nº 27/99 del 13 de Mayo de 1999 con la 
            perspectiva de formar profecionales calificados en el área de Ciencias de la Computacion.
        </p>
        <h4 class="pt-4" id="item-2">Misión</h4>
        <div class="text-center pt-2">
            <a><i class="fas fa-rocket fa-7x"></i></i></a>
        </div>  
        <p class="p-2">Formar profecionales a nivel de Licenciatura con sólida formacion ética y cultural, comprometidos con los cambios tecnológicos, 
            cientificos y humanisticos, aportando constantemente a al mejoramiento y creacion de tecnicas y metodos de los sistemas computacionales.
        </p>
        <h4 class="pt-4" id="item-3">Visión</h4>
        <div class="text-center pt-2">
            <a><i class="fas fa-eye fa-7x"></i></i></a>
        </div>
        <p class="p-2">Ser una carrera de excelencia reconocida en el medio, dedicada a formar profecionales a nivel licenciatura, 
            capaces de resolver problemas que involucren tecnologia computacional, con habilidad para administrar sistemas, proporcionar apoyo tecnico, 
            desarrollar y aplicar nuevos métodos y técnicas para la construccion de sistemas de software, vinculados a avances cientificos y tecnologicos,
             con valores éticos y responsabilidad socio.cultural.
        </p>
        <h4 class="pt-4" id="item-4">Objetivos Generales</h4>
        <div class="text-center pt-2">
            <a><i class="fas fa-shoe-prints fa-7x"></i></i></a>
        </div>
        <p class="p-2">Incorporar en el curriculum los nuevos desafios de la educación superior para responder las necesidades y desarrollo de la región 
            y del país, en el ámbito de la alta competitividad nacional e internacional revalorizando el conocimiento.
        </p class="p-2">
        <p>
            Incentivar la investigación científica y tecnológica de la carrera, en un ambiente favorable a la cultura de investigación que responda a las
             necesidades de la comunudad unuversitaria y las demandas en el desarrollo en la región y el país con calidad y pertinencia social.
        </p>
        <h4 class="pt-4" id="item-5">Perfil Profecional</h4>
        <div class="text-center pt-2">
            <a><i class="fas fa-user-tie fa-7x"></i></i></a>
        </div>
        <p class="p-2">
            El ingeniero de Sistemas debe ser capaz de planificar, diseñar, modelar, optimizar, implantar y administrar soluciones TIC variables multidisciplinarias
             con un enfoque sistémico, dentro de un marco institucional y organizativo.
        </p>
        <p class="p-2">
            Viable significa tener en cuenta la infraestructura de hardware, el softwarenecesario, la infraestructura de comunicaciones y los aspectos de seguridad necesarios para un sistema de esta naturaleza.
        </p>
        <p class="p-2">
            Multidisciplinacia significa que debe ser capaz de coordinar grupos de trabajo para relacionarse con las diferentes ramas del ser humano.
